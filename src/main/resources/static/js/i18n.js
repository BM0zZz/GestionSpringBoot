/* Obtener preferencias guardadas */
function getI18nSettings() {
  const saved = localStorage.getItem("panelSettings");

  if (!saved) {
    return {
      theme: "Oscuro",
      language: "Español"
    };
  }

  return {
    theme: "Oscuro",
    language: "Español",
    ...JSON.parse(saved)
  };
}

/* Obtener texto traducido */
function t(key) {
  const settings = getI18nSettings();
  const language = settings.language || "Español";

  return translations[language]?.[key] || translations["Español"][key] || key;
}

/* Aplicar idioma a todos los elementos traducibles */
function applyLanguage(language) {
  const selectedLanguage = language || getI18nSettings().language || "Español";

  if (selectedLanguage === "Inglés") {
    document.documentElement.setAttribute("lang", "en");
  } else {
    document.documentElement.setAttribute("lang", "es");
  }

  const translatableElements = document.querySelectorAll("[data-i18n]");

  translatableElements.forEach(element => {
    const key = element.getAttribute("data-i18n");
    element.textContent = translations[selectedLanguage]?.[key] || translations["Español"][key] || key;
  });

  const placeholderElements = document.querySelectorAll("[data-i18n-placeholder]");

  placeholderElements.forEach(element => {
    const key = element.getAttribute("data-i18n-placeholder");
    element.placeholder = translations[selectedLanguage]?.[key] || translations["Español"][key] || key;
  });
}

/* Aplicar idioma automáticamente al cargar la página */
document.addEventListener("DOMContentLoaded", () => {
  applyLanguage();
});

/* Hacer funciones accesibles para otros archivos */
window.t = t;
window.applyLanguage = applyLanguage;